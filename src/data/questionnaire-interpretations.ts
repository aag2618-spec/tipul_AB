/** רמה עשירה אחת — מותאמת לפי תווית הטווח ו/או החומרה. */
export interface RichLevel {
  /** מחזיר true אם הרמה הזו מתאימה לתווית/חומרה הנוכחית. */
  matches: (label: string, severity: string) => boolean;
  /** פסקאות משמעות קלינית (מוצגות כפסקאות נפרדות). */
  body: string[];
  /** "על מה לשים לב" — נקודות מעשיות. */
  watchFor?: string[];
  /** המלצות מעשיות. */
  recommendations?: string[];
  /** הכוונת מעקב. */
  followUp?: string;
}

/** אשכול פריטים המחושב מהתשובות הגולמיות (למשל קוגניטיבי מול סומטי). */
export interface RichCluster {
  key: string;
  name: string;
  itemIds: number[];
  interpret: (score: number, max: number) => string;
}

/** פרוטוקול לפריט קריטי — דגל לפי ערך התשובה. */
export interface CriticalProtocol {
  itemId: number;
  byValue: Record<number, RiskFlag>;
}

export interface InterpretationSpec {
  intro?: string;
  levels?: RichLevel[];
  clusters?: RichCluster[];
  criticalProtocol?: CriticalProtocol;
  // --- שכבות העשרה (פרסונליזציה / כלים / מעקב) ---
  /** זיהוי חתימה/דפוס המצוקה */
  detectPattern?: (ctx: RichContext) => PatternInfo | null;
  /** שאלות לבירור בפגישה הבאה */
  questionsToAsk?: (ctx: RichContext) => string[];
  /** יעדי טיפול מוצעים */
  treatmentTargets?: (ctx: RichContext) => string[];
  /** שאלונים משלימים מומלצים */
  complementary?: (ctx: RichContext) => ComplementarySuggestion[];
  /** כיווני אבחנה מבדלת לשלילה (לא אבחנה) */
  differential?: (ctx: RichContext) => string[];
  /** תסמיני ליבה (DSM) — האם התסמינים המרכזיים נוכחים */
  coreSymptoms?: (ctx: RichContext) => string;
  /** השפעה תפקודית — תחומי חיים שהתסמינים נוגעים בהם */
  functionalImpact?: (ctx: RichContext) => string[];
  /** פסיכו-חינוך — פסקה לשיתוף עם המטופל/משפחה */
  psychoeducation?: (ctx: RichContext) => string;
}

