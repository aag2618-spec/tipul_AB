// סולם 1-7. reverse=true הופך את הניקוד (score = 8 - value). score מוגדר תמיד
// כדי לשמור על תצוגה אחידה (בלי מספר ליד האפשרות).
function catqOpts(reverse: boolean) {
  const scale = [
    { v: 7, text: "מסכים/ה מאוד" },
    { v: 6, text: "מסכים/ה" },
    { v: 5, text: "נוטה להסכים" },
    { v: 4, text: "ניטרלי/ת" },
    { v: 3, text: "נוטה שלא להסכים" },
    { v: 2, text: "לא מסכים/ה" },
    { v: 1, text: "כלל לא מסכים/ה" },
  ];
  return scale.map((o) => ({ value: o.v, score: reverse ? 8 - o.v : o.v, text: o.text }));
}

