// שמות תת-הסולמות בעברית (לתצוגה).
const AQ_SUBSCALE_NAMES: Record<AqSection, string> = {
  social: "מיומנות חברתית",
  switching: "מעבר קשב / גמישות",
  detail: "תשומת לב לפרטים",
  communication: "תקשורת",
  imagination: "דמיון",
};

// בונה את מפת תת-הסולמות (items 1-based) מתוך בנק הפריטים — זהה לכל הגרסאות.
function buildAqSubscales() {
  const subs: Record<string, { items: number[]; name: string }> = {};
  (Object.keys(AQ_SUBSCALE_NAMES) as AqSection[]).forEach((sec) => {
    subs[sec] = { items: [], name: AQ_SUBSCALE_NAMES[sec] };
  });
  AQ_ITEMS.forEach((item, i) => subs[item.section].items.push(i + 1));
  return subs;
}

// אפשרויות ניקוד בינארי (0/1) — לגרסאות מבוגרים ונוער (טווח 0-50).
function aqBinaryOpts(dir: AqDir) {
  const agree = dir === "A" ? 1 : 0;
  const disagree = dir === "A" ? 0 : 1;
  return [
    { value: 3, score: agree, text: "מסכים/ה בהחלט" },
    { value: 2, score: agree, text: "מסכים/ה במידת מה" },
    { value: 1, score: disagree, text: "לא ממש מסכים/ה" },
    { value: 0, score: disagree, text: "כלל לא מסכים/ה" },
  ];
}

// אפשרויות ניקוד מלא (0-3) — לגרסת הילדים (AQ-Child, טווח 0-150).
function aqLikertOpts(dir: AqDir) {
  // dir "A": מסכים בהחלט=3 ... כלל לא=0.  dir "D": הפוך.
  const a = dir === "A";
  return [
    { value: 3, score: a ? 3 : 0, text: "מסכים/ה בהחלט" },
    { value: 2, score: a ? 2 : 1, text: "מסכים/ה במידת מה" },
    { value: 1, score: a ? 1 : 2, text: "לא ממש מסכים/ה" },
    { value: 0, score: a ? 0 : 3, text: "כלל לא מסכים/ה" },
  ];
}

function buildAqQuestions(variant: "adult" | "third", scale: "binary" | "likert") {
  return AQ_ITEMS.map((item, i) => ({
    id: i + 1,
    section: item.section,
    title: variant === "adult" ? item.adult : item.third,
    options: scale === "binary" ? aqBinaryOpts(item.dir) : aqLikertOpts(item.dir),
  }));
}

